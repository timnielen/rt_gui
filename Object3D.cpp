Object3D::Object3D() {
	glGenVertexArrays(1, &VAO);
	unsigned int VBO;
	unsigned int EBO;

	glGenBuffers(1, &VBO);
	glGenBuffers(1, &EBO);

	glBindVertexArray(VAO);

	glBindBuffer(GL_ARRAY_BUFFER, VBO);
	glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

	glBindBuffer(GL_ELEMENT_ARRAY_BUFFER, EBO);
	glBufferData(GL_ELEMENT_ARRAY_BUFFER, sizeof(indices), indices, GL_STATIC_DRAW);

	size_t stride = 8 * sizeof(float);
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, stride, (void*)0);
	glEnableVertexAttribArray(0);
	glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, stride, (void*)(3 * sizeof(float)));
	glEnableVertexAttribArray(1);
	glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, stride, (void*)(6 * sizeof(float)));
	glEnableVertexAttribArray(2);


	diffuseMap = load_texture("img/container2.png");
	specularMap = load_texture("img/container2_specular.png");
}

void Object3D::updateTranformationMatrix() {
	transform = glm::mat4(1.0f);
	transform = glm::translate(transform, position);
	transform = glm::rotate(transform, angle, axis);
	transform = glm::scale(transform, scale);
}

void Object3D::setPosition(glm::vec3 p) {
	position = p;
	updateTranformationMatrix();
}
void Object3D::setRotationAxis(glm::vec3 a) {
	axis = a;
	updateTranformationMatrix();
}
void Object3D::setRotationAngle(float a) {
	angle = a;
	updateTranformationMatrix();
}
void Object3D::setScale(float s) {
	scale = glm::vec3(s);
	updateTranformationMatrix();
}

void Object3D::setScale(glm::vec3 s) {
	scale = s;
	updateTranformationMatrix();
}

void Object3D::setMaterial(const Material& m) {
	material = m;
}

void Object3D::render(const Shader &shader) {
	shader.setMat4("transform", transform);
	shader.setInt("material.diffuse", 0);
	shader.setInt("material.specular", 1);
	shader.setFloat("material.shininess", material.shininess);

	glActiveTexture(GL_TEXTURE0);
	glBindTexture(GL_TEXTURE_2D, diffuseMap);
	glActiveTexture(GL_TEXTURE1);
	glBindTexture(GL_TEXTURE_2D, specularMap);
	glBindVertexArray(VAO);
	//glDrawElements(GL_TRIANGLES, 6, GL_UNSIGNED_INT, 0);
	glDrawArrays(GL_TRIANGLES, 0, 36);
}